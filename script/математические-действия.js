let num1 = 404;
let num2 = 2;

console.log(num1 + num2); // сложение
console.log(num1 - num2); // вычитание
console.log(num1 * num2); // умножение
console.log(num1 / num2); // деление
console.log(num1 % num2); // остаток от деления 

let num3 = 10;
num3 = num3 + 90; // num3 = 100
// сокращенная запись 
let num4 = 10;
num4 += 90 // num4 = 100
// добавить еденицу
num4 ++; //num4 = 101;

